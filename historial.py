class Historial(cita, stack):
    def __init__(self):
        self.historial = []
        self.historial.push(Cita.__init__(nombre)) 
        self.historial.push(Cita.__init__(apellido))
        self.historial.push(Cita.__init__(edad))
        self.historial.push(Cita.__init__(fecha))
        self.historial.push(Cita.__init__(enfermedad))
        self.historial.push(Cita.__init__(receta))
        return self.historial
